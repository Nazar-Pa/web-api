// Build Turbo.az URL with nested params
function buildTurboUrl(filters = {}) {
  const baseUrl = "https://turbo.az/autos";
  const params = new URLSearchParams();

  // Helper to append array params like q[model][]
  const appendArray = (key, values) => {
    if (!values) return;
    values.forEach(v => {
      params.append(`q[${key}][]`, v);
    });
  };

  // Basic filters
  if (filters.page) params.append("page", filters.page);
  if (filters.make) appendArray("make", [filters.make]);
  if (filters.models) appendArray("model", filters.models);

  if (filters.yearFrom) params.append("q[year_from]", filters.yearFrom);
  if (filters.yearTo) params.append("q[year_to]", filters.yearTo);

  if (filters.priceFrom) params.append("q[price_from]", filters.priceFrom);
  if (filters.priceTo) params.append("q[price_to]", filters.priceTo);

  // Defaults (important!)
  // params.append("q[currency]", "azn");
  // params.append("q[loan]", "0");
  // params.append("q[barter]", "0");

  console.log("Built URL:", `${baseUrl}?${params.toString()}`);

  return `${baseUrl}?${params.toString()}`;
}

// Parse listings
function parseListings(html) {
  const $ = cheerio.load(html);
  const cars = [];

  const productsAmountText = $(".products-title-amount").first().text().trim();
  const productsAmount = parseInt(productsAmountText.replace(/[^\d]/g, ""), 10);
  const perPage = 24;
  const totalPages = Math.ceil(productsAmount / perPage);

  $(".products-i:not(.products-i--promotion-card)").each((i, el) => {
    const item = $(el);

    // 🔼 TOP section (image, badges)
    const top = item.find(".products-i__top");

    const image =
      top.find("img").attr("src") ||
      top.find("img").attr("data-src");

    const isVip = top.find(".products-i__paid").length > 0;

    // 🔽 BOTTOM section (main data)
    const bottom = item.find(".products-i__bottom");

    const name = bottom.find(".products-i__name").text().trim();
    const priceText = bottom.find(".products-i__price").text().trim();
    const attr = bottom.find(".products-i__attributes").text().trim();
    const datetime = bottom.find(".products-i__datetime").text().trim();

    const link = item.find("a.products-i__link").attr("href");
    let id = null;
    if (link) {
      const slug = link.split("/").pop(); // "10149130-audi-a4"
      id = slug.split("-")[0];            // "10149130"
    }
    // Parse attributes
    let year, engine, mileage;

    if (attr) {
      const parts = attr.split(",").map(p => p.trim());
      year = parts[0];
      engine = parts[1];
      mileage = parts[2];
    }

    // Clean price
    const price = parseInt(priceText.replace(/[^\d]/g, ""), 10);

    cars.push({
      id,
      name,
      price,
      currency: "AZN",
      year,
      engine,
      mileage,
      image,
      isVip,
      location_time: datetime,
      link: link ? `https://turbo.az${link}` : null
    });
  });

  return {cars, productsAmount, totalPages};
}

// API endpoint
app.get("/api/turbo", async (req, res) => {
  try {
    const {
      make,
      models, // comma separated: "14,group346"
      yearFrom,
      yearTo,
      priceFrom,
      priceTo,
      page
    } = req.query;

    const filters = {
      make,
      models: models ? models.split(",") : undefined,
      yearFrom,
      yearTo,
      priceFrom,
      priceTo,
      page
    };

    const url = buildTurboUrl(filters);

    const response = await axios.get(url, {
      headers: {
        "User-Agent": "Mozilla/5.0",
        "Accept-Language": "az,en;q=0.9"
      }
    });

    const data = parseListings(response.data);

    res.json({
      success: true,
      url,
      count: data.productsAmount,
      totalPages: data.totalPages,
      data: data.cars
    });

  } catch (err) {
    console.error(err.message);

    res.status(500).json({
      success: false,
      error: "Scraping failed"
    });
  }
});

